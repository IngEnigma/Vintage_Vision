        versionCode = flutter.versionCode?.toInt() ?: 3
        versionName = flutter.versionName ?: "1.2"
    }

    signingConfigs {
        create("release") {
            keyAlias = keystoreProperties["keyAlias"] as String
            keyPassword = keystoreProperties["keyPassword"] as String
